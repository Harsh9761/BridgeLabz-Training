import java.util.*;
public class avgOfThree {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        int c = sc.nextInt();
        int ans = (a+b+c)/3;
        System.out.print(ans);
    }
}
